package actionItem;

public class Action_item_practice {
    public static void main(String[] args) {

        String[] Flowers = new String[4];

        Flowers[0] = "Rose";
        Flowers[1] = "Tulip";
        Flowers[2] = "Sunflower";
        Flowers[3] = "Lavender";

        for (int i = 0; i < Flowers.length; i++) {


            if (Flowers[i] == "Rose") {
                System.out.println(Flowers[i]);
            }
            if (Flowers[i] == "Sunflower") {
                System.out.println(Flowers[i]);
            }
        }
    }
}
